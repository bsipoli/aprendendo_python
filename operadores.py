# Em Python, os principais operadores são:
# + (Adição): Soma dois valores.
# - (Subtração): Subtrai um valor do outro.
# * (Multiplicação): Multiplica dois valores.
# / (Divisão): Divide um valor pelo outro. O resultado é sempre um número decimal (float).
# //(Divisão Inteira): Divide um valor pelo outro e ignora a parte decimal.
# % (Módulo): Retorna o resto da divisão. Exemplo: 10 % 3 é 1, porque 10 dividido por 3 é 3 e sobra 1.
# **(Exponenciação): Eleva um número a uma potência. Exemplo: 2 ** 3 é 8 (2 elevado a 3).

